package com.sadiafoodsuggester.inzamam.foodsuggester;

public class Users {
    String email, username, age, height, weight, gender;

    public Users(){

    }

    public Users(String email, String username, String age, String height, String weight, String gender) {
        this.email = email;
        this.username = username;
        this.age = age;
        this.height = height;
        this.weight = weight;
        this.gender = gender;
    }
}
